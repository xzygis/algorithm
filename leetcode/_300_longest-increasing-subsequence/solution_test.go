package _300_longest_increasing_subsequence

import "testing"

func TestLengthOfLIS(t *testing.T) {
	tests := []struct {
		name string
		nums []int
		want int
	}{
		// 示例测试
		{"示例1", []int{10, 9, 2, 5, 3, 7, 101, 18}, 4},
		{"示例2", []int{0, 1, 0, 3, 2, 3}, 4},
		{"示例3", []int{7, 7, 7, 7, 7, 7, 7}, 1},

		// 边界条件
		{"单元素", []int{5}, 1},
		{"空数组", []int{}, 0}, // 虽然提示长度>=1，但可测试

		// 严格递增
		{"严格递增", []int{1, 2, 3, 4, 5}, 5},
		{"严格递减", []int{5, 4, 3, 2, 1}, 1},

		// 包含重复
		{"重复与递增混合", []int{1, 2, 2, 3, 4}, 4}, // 最长可以是[1,2,3,4]
		{"波浪形", []int{1, 3, 2, 4, 3, 5}, 4},  // 最长可以是[1,2,3,5]或[1,3,4,5]

		// 随机复杂案例
		{"复杂案例1", []int{3, 5, 6, 2, 5, 4, 19, 5, 6, 7, 12}, 6},                    // 最长可以是[3,5,6,7,12]或[2,4,5,6,7,12]等
		{"复杂案例2", []int{0, 8, 4, 12, 2, 10, 6, 14, 1, 9, 5, 13, 3, 11, 7, 15}, 6}, // 经典例子，最长长度为6

		// 负数与零
		{"含负数", []int{-2, -1, 0, 1, 2}, 5},
		{"负递减", []int{2, 1, 0, -1, -2}, 1},

		// [1,3,6,7,9,4,10,5,6]
		{"test", []int{1, 3, 6, 7, 9, 4, 10, 5, 6}, 6},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := lengthOfLIS(tt.nums); got != tt.want {
				t.Errorf("lengthOfLIS(%v) = %v, want %v", tt.nums, got, tt.want)
			}
		})
	}
}
